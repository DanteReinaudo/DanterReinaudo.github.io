function changeLanguage() {
    const lang = document.documentElement.lang;
    const new_lang = lang === "es" ? "en" : "es";
    setLanguage(new_lang)
    console.log("change language succesfully")
}

function setLanguage(lang) {
    var keys = Object.keys(translation[lang])
    console.log(keys)
    keys.forEach(function (key){
        var element = document.getElementById(key)
        if (translation[lang][key] && element) {
            element.textContent = translation[lang][key];
        }
    })
    document.documentElement.lang = lang;
    localStorage.setItem('lang', lang);
    console.log("lenguage set succesfully")
}


function getCurriculumFile(lang) {
    return (lang === 'es') ? 'cv/cv_es.pdf' : 'cv/cv_en.pdf';
}

function downloadCV() {
    const lang = document.documentElement.lang;
    const file_name = getCurriculumFile(lang);
    document.getElementById('button_download').href = file_name;
}

document.getElementById('button_download').addEventListener('click', downloadCV);

